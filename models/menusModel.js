import mongoose from "mongoose";

const { Schema } = mongoose;

const menusSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const Menus = mongoose.model("menus", menusSchema);
export default Menus;
